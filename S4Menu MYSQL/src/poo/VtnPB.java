    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAceptarActionPerformed
    {//GEN-HEADEREND:event_btnAceptarActionPerformed
        if (btnAceptar.getText().equals("Buscar"))
        {
            String nom = JOptionPane.showInputDialog(this,
                    "dame el nombre a eliminar :");
            Connection con = ManipulaDB.conecta(this);
            if (con != null)
            {
                Querys sql = new Querys();
                ArrayList<Perecederos> ap = ManipulaDB.cargaP(sql.Seleccion(con, "*", "Perecederos", "nombre='" + nom.trim() + "'"));
                ManipulaDB.desconecta(con);
                if (ap != null)
                {
                    tfNombre.setText(ap.get(0).getNom());
                    tfExistenacias.setText(String.valueOf(ap.get(0).getExistencias()));
                    tfFechaCad.setText( ap.get(0).getFechaCad());
                    btnAceptar.setText("Eliminar");
                }else
                {
                    Mensaje.error(this, "Dato no encontrado");
                }
            }
        } else
        {
            if (Mensaje.pregunta(this, "Â¿desea eliminar este registro?") == 0)
            {
                Connection con = ManipulaDB.conecta(this);
                if (con != null)
                {
                     Querys sql = new Querys();
                     sql.Delete(con, "Perecederos", "nombre", "'"+tfNombre.getText().trim()+"'");
                     ManipulaDB.desconecta(con);
                     Mensaje.exito(this, "Dato eliminado correctamente");
                }
            }
            btnCancelarActionPerformed(evt);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

