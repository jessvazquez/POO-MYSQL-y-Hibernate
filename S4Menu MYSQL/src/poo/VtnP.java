    public static int busca(int opc, String s, JFrame jf)
    {
        if (p == null)
        {
            Mensaje.error(jf, "No hay datos almacenados...");
        } else
        {
            String nom = JOptionPane.showInputDialog(jf,
                    "dame el nombre a " + s + " :");
            if (nom != null)
            {
                for (int i = 0; i < p.length; i++)
                {
                    if (p[i] instanceof Perecederos
                            && opc == 1 && nom.equals(p[i].getNom()))
                    {
                        return i;
                    } else
                    {
                        if (p[i] instanceof NoPerecederos
                                && opc == 2 && nom.equals(p[i].getNom()))
                        {
                            return i;
                        }
                    }
                }
                Mensaje.error(jf, "Dato no encontrado...");
            }
        }
        return -1;
    }

